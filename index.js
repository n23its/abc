const BASE_TEMPLATE = `ॐ विष्णुर्विष्णुर्विष्णुः॥ श्रीमद्भगवतो महापुरुषस्य श्रीमहाविष्णोराज्ञया प्रवर्तमानस्य अद्य श्रीब्रह्मणो {{brahma_parardha}} {{kalpa}} {{manvantara}} {{yuga}} {{kali_charana}} {{loka}} {{dvipa}} {{varsha}} {{khanda}} {{aryavarta}} देवब्राह्मणगुरुसज्जनसन्निधौ श्रीमन्नृपतिविक्रमादित्यराज्यसमयात् {{kshetra}}-क्षेत्रे || {{pradesha}}-प्रदेशे || {{nagara}} नगरे || {{tirth}} -तीर्थे || प्रवर्तमानस्य श्री शुभ विक्रम संवत {{vikram_samvat}}|| शालिवाहन शक {{shaq_samvat}} || {{samvatsara_name}}-नाम-संवत्सरे || {{ayana}} || {{masa}}-मासे || {{paksha}}-पक्षे || {{tithi}}-तिथौ || {{vara}}-वासरे || {{nakshatra}}-नक्षत्रे || {{yoga}}-योगे || {{karana}}-करणे || {{chandra_rashi}}-राशिस्थिते चन्द्रे || {{surya_rashi}}-राशिस्थिते श्रीसूर्ये || शेषेषु ग्रहेषु यथायथं राशिस्थानस्थितेषु`;

function renderTemplate(template, data) {
  let output = template;

  Object.keys(data).forEach((key) => {
    const regex = new RegExp(`{{${key}}}`, "g");

    output = output.replace(regex, data[key]);
  });

  return output;
}

function generateSankalpa(ritualKey) {
  const ritual = sankalpaCollection[ritualKey];

  if (!ritual) {
    return;
  }

  const finalOutput =
    renderTemplate(BASE_TEMPLATE, GLOBAL_DATA) + ritual.template;

  document.getElementById("output").innerText = finalOutput;
}

function createButtons() {
  const container = document.getElementById("ritualButtons");

  Object.keys(sankalpaCollection).forEach((key) => {
    const ritual = sankalpaCollection[key];

    const button = document.createElement("button");

    button.innerText = ritual.title;

    button.onclick = () => generateSankalpa(key);

    container.appendChild(button);
  });
}

